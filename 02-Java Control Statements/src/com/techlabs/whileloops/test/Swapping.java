package com.techlabs.whileloops.test;

public class Swapping {
	public static void main(String args[]) {
		
		int number1 = 2;
		int number2 = 10;
		int temporary = 0;
		
		temporary = number1;
		number1 = number2;
		number2 = temporary;
		System.out.println("Number1: "+number1);
		System.out.println("Number2: "+number2);
		
		
	}
}
